package basicprograms;

public class forLoop_Basics {

	public static void main(String[] args) {

		
		//Example 1
		
		for(int i=0;i<=13;i++)
		{
			System.out.println(i);
		}
		
		
		//Example 2
		
		for (int j=1; j<=25;j++)
		{
			if(j%2==0)
			{
			System.out.println("The number is even: " + j);
			}else {
				
				System.out.println("The number is odd: " +j);
			}
			
		}
		
		
		
		//Example 3
		
		for(int m=20;m>=0;m--)
		{
			System.out.println(m);
		}
		
		
		
		
		
		
	}

}
