package Polymorphism;

public class PolymorphismMethods {
	
	
	public int x=10;
	public int y=20;
	
	public void adder()
	{
		System.out.println(x+y);
	}
	
	public void adder(int a, int b)
	{
		System.out.println(a+b);
	}
	
	public void adder(int x, double y)
	{
		System.out.println(x+y);
	}
	
	public void adder(int x, int y, int z)
	{	
		System.out.println(x+y+z);
		
	}

}
