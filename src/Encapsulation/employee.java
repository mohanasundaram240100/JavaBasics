package Encapsulation;

public class employee {
	
	private String name;
	private int age;
	
	
	//getter and setter method for name
	public String getname()
	{
		return name;
	}
	
	public String setname(String name)
	{
		this.name=name;
		return name;
	}

	//getter and setter method for age
	
	public int getage()
	{
		return age;
	}
	
	public int setage(int age)
	{
		this.age=age;
		return age;
	}
	
	
	
	
	

}
